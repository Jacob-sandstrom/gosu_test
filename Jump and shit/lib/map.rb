
class Map
    def initialize
        @block_img = Gosu::Image.new("../img/Block.png")
        $block_size = 32

        
        $map_string =   "........................." + 
                        "........................." +
                        "..####...........####...." +
                        "........................." +
                        "........................." +
                        "..........####..........." + 
                        "........................." +
                        "..................####..." + 
                        ".....................##.." +
                        "........................." +
                        "..........#####.........." +
                        "##.....................##" +
                        "................#........" +
                        "...##...............#...." +
                        "...####............###..." +
                        ".....####.........#####.." + 
                        "......###........#######." +
                        "........................." +
                        "#########################" 

    end

    def draw

        i = 0
        while i < 19
            j = 0
            while j < 25
                if $map_string[j + i*25] == "#"
                @block_img.draw(j*$block_size, i*$block_size, 0)
                end
                j += 1
            end
            i += 1
        end


    end



end